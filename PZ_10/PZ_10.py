#Исходные данные
voyage = {"Мексика", "Канада", "Израиль", "Италия", "США"}
reina_tour = {"Англия", "Япония", "Канада", "ЮАР"}
raduga = {"США", "Испания", "Швеция", "Австралия", "Италия", "Канада"}


agencies_with_both = []
if "Италия" in voyage and "Канада" in voyage:
    agencies_with_both.append("Вояж")
if "Италия" in reina_tour and "Канада" in reina_tour:
    agencies_with_both.append("РейнаТур")
if "Италия" in raduga and "Канада" in raduga:
    agencies_with_both.append("Радуга")

print("1. Турагентства с Италией и Канадой:", agencies_with_both if agencies_with_both else "Нет таких")


reina_tour.add("Индия")
print("2. РейнаТур после добавления Индии:", reina_tour)


all_tours = voyage.union(reina_tour).union(raduga)
print("3. Полный список всех туров:", all_tours)